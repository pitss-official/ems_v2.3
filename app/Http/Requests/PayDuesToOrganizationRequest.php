<?php

namespace App\Http\Requests;

use App\OnlinePayment;
use App\System;
use Illuminate\Foundation\Http\FormRequest;

class PayDuesToOrganizationRequest extends FormRequest
{
    /**
     * Determine if the user is authorized to make this request.
     *
     * @return bool
     */
    public function authorize()
    {
        return $this->user('api')->can('payDues',OnlinePayment::class);
    }

    /**
     * Get the validation rules that apply to the request.
     *
     * @return array
     */
    public function rules()
    {
        return [
            'amount'=>'bail|required|numeric|min:0|max:100000'
        ];
    }
    public function validatedAndSanitized(array $skippedKeys=[])
    {
        return System::sanitize($this->validated(),$skippedKeys);
    }
}
